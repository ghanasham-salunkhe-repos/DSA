package strings;

public class EscapeCharactersInJava {
    // in java we have escape sequences used inside string
    public static void main(String[] args) {

        System.out.println("ghansham is good & \"sincere\""); // \" used in string to write "
        System.out.println("ghansham is good & \'sincere\'");// \' used for ' (not needed inside "")
        System.out.println("ghansham \\ salunkhe \\ is \\ here");// \\ used for \

////        similarly we have few more
//        \n	New Line
//        \r	Carriage Return
//        \t	Tab
//        \b	Backspace
//        \f	Form Feed

    }
}
